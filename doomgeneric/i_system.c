//
// I_Quit
//

void I_Quit (doom_data_t* doom)
{
    doom->should_quit = 1;
    atexit_listentry_t *entry;

    // Run through all exit functions
 
    entry = exit_funcs; 

    while (entry != NULL)
    {
        entry->func(doom);
        entry = entry->next;
    }
}

